package com.hp.dbpowerpack.Model;


/**
 * The Class ObjCodeViewModel.
 */
public class ObjCodeViewModel {

	/** The db1 code. */
	private String db1Code;

	/** The db2 code. */
	private String db2Code;

	/** The color flag. */
	private String colorFlag;

	/**
	 * Gets the color flag.
	 *
	 * @return the color flag
	 */
	public String getColorFlag() {
		return colorFlag;
	}

	/**
	 * Sets the color flag.
	 *
	 * @param colorFlag the new color flag
	 */
	public void setColorFlag(String colorFlag) {
		this.colorFlag = colorFlag;
	}

	/**
	 * Gets the db1 code.
	 *
	 * @return the db1 code
	 */
	public String getDb1Code() {
		return db1Code;
	}

	/**
	 * Sets the db1 code.
	 *
	 * @param db1Code the new db1 code
	 */
	public void setDb1Code(String db1Code) {
		this.db1Code = db1Code;
	}

	/**
	 * Gets the db2 code.
	 *
	 * @return the db2 code
	 */
	public String getDb2Code() {
		return db2Code;
	}

	/**
	 * Sets the db2 code.
	 *
	 * @param db2Code the new db2 code
	 */
	public void setDb2Code(String db2Code) {
		this.db2Code = db2Code;
	}
}
